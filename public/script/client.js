let logout = document.getElementById("logout");


logout.addEventListener("click", (e) => {
    fetch("/logout", {
     
        // Adding method type
        method: "POST",
         
        // Adding body or contents to send
        body: JSON.stringify({
           status: "Success"
        }),
         
        // Adding headers to the request
        headers: {
            "Content-type": "application/json; charset=UTF-8"
        }
    })
});



 